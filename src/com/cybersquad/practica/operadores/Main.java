package com.cybersquad.practica.operadores;

public class Main {
    public static void main(String[] args) {
        //Operadores aritméticos
        //Suma
        int sum = 10 + 5;
        System.out.println("Suma: " + sum);

        //Resta
        int subtraction = 10 - 5;
        System.out.println("Resta: " + subtraction);

        //Multiplicación
        int multiplication = 10 * 5;
        System.out.println("Multiplicación: " + multiplication);

        //División
        int division = 10 / 5;
        System.out.println("División: " + division);

        //Módulo
        int module = 10 % 5;
        System.out.println("Módulo: " + module);

        //Operadores de asignación

        //Asignación
        int assignment = 10;
        System.out.println("Asignación: " + assignment);

        //Suma y asignación
        int sumAssignment = 10;
        sumAssignment += 5;
        System.out.println("Suma y asignación: " + sumAssignment);

        //Resta y asignación
        int subtractionAssignment = 10;
        subtractionAssignment -= 5;
        System.out.println("Resta y asignación: " + subtractionAssignment);

        //Multiplicación y asignación
        int multiplicationAssignment = 10;
        multiplicationAssignment *= 5;
        System.out.println("Multiplicación y asignación: " + multiplicationAssignment);

        //División y asignación
        int divisionAssignment = 10;
        divisionAssignment /= 5;
        System.out.println("División y asignación: " + divisionAssignment);

        //Módulo y asignación
        int moduleAssignment = 10;
        moduleAssignment %= 5;
        System.out.println("Módulo y asignación: " + moduleAssignment);

        //Operadores de comparación

        //Igual a
        boolean equalTo = 10 == 5;
        System.out.println("Igual a: " + equalTo);

        //Diferente de
        boolean differentFrom = 10 != 5;
        System.out.println("Diferente de: " + differentFrom);

        //Mayor que
        boolean greaterThan = 10 > 5;
        System.out.println("Mayor que: " + greaterThan);

        //Menor que
        boolean lessThan = 10 < 5;  
    }
}
